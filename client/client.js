Template.uploadfile.admin = function () {
  if (Meteor.user() == undefined || Meteor.user().roles == undefined){
    return false;
  }
  if (Meteor.user().roles[0].indexOf("admin") != -1 &&
      Meteor.user().roles[1].indexOf("user-admin") != -1)
    return true;
  else
    return false;
};


